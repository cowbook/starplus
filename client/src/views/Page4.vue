export default {
  name: 'Page4',
  data() {
    const hostname = window.location.hostname;
    const isLocalHost =
      hostname === 'localhost' ||
      hostname === '127.0.0.1' ||
      /^\d+\.\d+\.\d+\.\d+$/.test(hostname);

    return {
      assetsReady: false,
      choice: "жҒ’жұҮеӣҪйҷ…еӨ§еҺҰ",
      p1,p2,p3,p4,p4,p5,p6,p7,p8,
      logo,
      textTop,
      btnStar,
      btnDown,
      arrow,
      footerText,
      bgColor,
      bgLeft,
      bgRight,
      bgLine,
      apiBase:
        import.meta.env.VITE_API_URL ||
        (isLocalHost
          ? `${window.location.protocol}//${hostname}:3000/api`
          : '/api'),
      showHomeConfirm: false,
      submitting: false
    }
  },
  async mounted() {
    await this.preloadPageImages();
    this.assetsReady = true;

    this.$nextTick(() => {
      setTimeout(() => {
        document.querySelector('.page4')?.classList.add('appeared');
      }, 200);
    });

    const formStore = useFormStore();

    // д»Һ store иҺ·еҸ–ж•°жҚ®е№¶и®ҫзҪ®еҲ°жң¬ең° choice
    this.choice = formStore.formData?formStore.formData['зү©дёҡ']:'';
  },
  methods: {
    preloadPageImages() {
      const imageUrls = [
        this.logo,
        this.textTop,
        this.btnStar,
        this.btnDown,
        this.footerText,
        this.bgColor,
        this.bgLeft,
        this.bgRight,
        this.bgLine,
        this.p1,
        this.p2,
        this.p3,
        this.p4,
        this.p5,
        this.p6,
        this.p7,
        this.p8
      ];

      return Promise.all(
        imageUrls.map((url) =>
          new Promise((resolve) => {
            const img = new Image();
            img.onload = resolve;
            img.onerror = resolve;
            img.src = url;
          })
        )
      );
    },
    scrollToBottom() {
      window.scrollTo({
        top: document.documentElement.scrollHeight,
        behavior: 'smooth'
      });
    },
    
    goBack() {



      this.$router.push('/page2')
    },
    openHomeConfirm() {
      this.showHomeConfirm = true;
    },
    closeHomeConfirm() {
      this.showHomeConfirm = false;
    },
    confirmGoHome() {
      this.showHomeConfirm = false;
      this.$router.push('/home');
    },
    
    async blockTap(name) {

       if (this.submitting) {
         return;
       }

       this.choice = name;

       const formStore = useFormStore();
       formStore.formData = { ...formStore.formData,  'зү©дёҡ': name };

       console.log("йҖүжӢ©дәҶ", formStore.formData);
       this.submitting = true;

       try {
         const response = await fetch(`${this.apiBase}/submit`, {
           method: 'POST',
           headers: {
             'Content-Type': 'application/json'
           },
           body: JSON.stringify(formStore.formData)
         });

         if (!response.ok) {
           const errText = await response.text();
           alert(`жҸҗдәӨеӨұиҙҘ(${response.status})пјҡ${errText || 'иҜ·зЁҚеҗҺйҮҚиҜ•'}`);
           this.submitting = false;
           return;
         }

         const result = await response.json();
         const savedId = result?.id || result?.data?.id || '';
         formStore.formData = {
           ...formStore.formData,
           submissionId: savedId
         };

         setTimeout(() => {
           this.$router.push({
             path: '/page5',
             query: savedId ? { id: savedId } : {}
           });
         }, 700);
       } catch (error) {
         console.error('Error:', error);
         alert(`жҸҗдәӨеӨұиҙҘпјҢиҜ·жЈҖжҹҘзҪ‘з»ңжҲ–зЁҚеҗҺеҶҚиҜ•\n${error?.message || ''}`);
         this.submitting = false;
         return;
       }

       this.submitting = false;

    }
  }
}
</script>

